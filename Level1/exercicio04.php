<?php
/* vamos entender a estrutura condicional
ela é representada pelos comandos if e else
com parâmetros
parametros são o que estão entre parentêse
pesquise sobre tabela verdade
*/ 

$nome="felipe";
/*o parãmetro aqui é que: se (if) a variável ($nome) for igual (==) a "felipe" então ({}) escreva "o nome está certo"*/
